function выполнитьСериюОпераций() {
    перваяОперация()
        .then((результат1) => {
            console.log('Первая операция завершена:', результат1);
            return втораяОперация(результат1);
        })
        .then((результат2) => {
            console.log('Вторая операция завершена:', результат2);
            return третьяОперация(результат2);
        })
        .then((результат3) => {
            console.log('Третья операция завершена:', результат3);
        })
        .catch((ошибка) => {
            console.error('Произошла ошибка:', ошибка);
        });
}

function перваяОперация() {
    return new Promise((resolve, reject) => {
        resolve('результат первой операции');
    });
}

function втораяОперация(результат1) {
    return new Promise((resolve, reject) => {
        resolve('результат второй операции');
    });
}

function третьяОперация(результат2) {
    return new Promise((resolve, reject) => {
        resolve('результат третьей операции');
    });
}